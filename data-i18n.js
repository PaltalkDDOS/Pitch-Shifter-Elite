// === TOAST SIÊU TRONG SUỐT – NHƯ KHÔNG KHÍ + NEON SIÊU SÁNG ===
function showLanguageToast(messageKey) {
    const now = Date.now();
    if (now - lastToastTime < 100) return;
    lastToastTime = now;
    let toast = document.getElementById('lang-toast');
    if (!toast) {
        toast = document.createElement('div');
        toast.id = 'lang-toast';
        toast.className = 'overlay';
        document.body.appendChild(toast);
    }
    const dict = translations[currentLang];
    const message = dict[messageKey] || messageKey;
    toast.textContent = message;
    toast.classList.remove('neon');
    void toast.offsetWidth;
    toast.classList.add('neon');
    clearTimeout(toast.hideTimer);
    toast.hideTimer = setTimeout(() => {
        toast.classList.remove('neon');
        setTimeout(() => {
            toast.textContent = '';
        }, 450);
    }, 1600);
}
